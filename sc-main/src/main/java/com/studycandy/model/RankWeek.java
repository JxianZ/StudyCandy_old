package com.studycandy.model;

public class RankWeek {
    private Integer id;

    private Integer userId;

    private Integer signScore;

    private Integer noteScore;

    private Integer courseScore;

    private Integer taskScore;

    private Integer totalScore;

    private Integer finalScore;

    private Integer rankWeek;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getUserId() {
        return userId;
    }

    public void setUserId(Integer userId) {
        this.userId = userId;
    }

    public Integer getSignScore() {
        return signScore;
    }

    public void setSignScore(Integer signScore) {
        this.signScore = signScore;
    }

    public Integer getNoteScore() {
        return noteScore;
    }

    public void setNoteScore(Integer noteScore) {
        this.noteScore = noteScore;
    }

    public Integer getCourseScore() {
        return courseScore;
    }

    public void setCourseScore(Integer courseScore) {
        this.courseScore = courseScore;
    }

    public Integer getTaskScore() {
        return taskScore;
    }

    public void setTaskScore(Integer taskScore) {
        this.taskScore = taskScore;
    }

    public Integer getTotalScore() {
        return totalScore;
    }

    public void setTotalScore(Integer totalScore) {
        this.totalScore = totalScore;
    }

    public Integer getFinalScore() {
        return finalScore;
    }

    public void setFinalScore(Integer finalScore) {
        this.finalScore = finalScore;
    }

    public Integer getRankWeek() {
        return rankWeek;
    }

    public void setRankWeek(Integer rankWeek) {
        this.rankWeek = rankWeek;
    }
}